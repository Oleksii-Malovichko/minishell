#include "../../minishell.h"

int	print_env(char ***myenvp)
{
	int	i;

	i = 0;
	if (!*myenvp)
		return (1);
	while ((*myenvp)[i])
	{
		write(1, (*myenvp)[i], ft_strlen((*myenvp)[i]));
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
